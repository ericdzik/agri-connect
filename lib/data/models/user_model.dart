import '../../domain/entities/user.dart';

class UserModel extends User {
  const UserModel({
    required String id,
    required String nom,
    required String email,
    String? telephone,
    String? role,
    String? token,
    String? refreshToken,
    String? type,
    bool? emailVerifie,
    String? adresse,
    String? ville,
    String? codePostal,
    String? pays,
    String? avatar,
    String? nomExploitation,
    String? descriptionExploitation,
    bool? certifieBio,
    bool? verifie,
  }) : super(
         id: id,
         nom: nom,
         email: email,
         telephone: telephone,
         role: role,
         token: token,
         refreshToken: refreshToken,
         type: type,
         emailVerifie: emailVerifie,
         adresse: adresse,
         ville: ville,
         codePostal: codePostal,
         pays: pays,
         avatar: avatar,
         nomExploitation: nomExploitation,
         descriptionExploitation: descriptionExploitation,
         certifieBio: certifieBio,
         verifie: verifie,
       );

  factory UserModel.fromJson(Map<String, dynamic> json) => UserModel(
    id: json['id'] as String,
    nom: json['nom'] as String,
    email: json['email'] as String,
    telephone: json['telephone'] as String?,
    role: json['role'] as String?,
    token: json['token'] as String?,
    refreshToken: json['refreshToken'] as String?,
    type: json['type'] as String?,
    emailVerifie: json['emailVerifie'] as bool?,
    adresse: json['adresse'] as String?,
    ville: json['ville'] as String?,
    codePostal: json['codePostal'] as String?,
    pays: json['pays'] as String?,
    avatar: json['avatar'] as String?,
    nomExploitation: json['nomExploitation'] as String?,
    descriptionExploitation: json['descriptionExploitation'] as String?,
    certifieBio: json['certifieBio'] as bool?,
    verifie: json['verifie'] as bool?,
  );

  Map<String, dynamic> toJson() => {
    'id': id,
    'nom': nom,
    'email': email,
    'telephone': telephone,
    'role': role,
    'token': token,
    'refreshToken': refreshToken,
    'type': type,
    'emailVerifie': emailVerifie,
    'adresse': adresse,
    'ville': ville,
    'codePostal': codePostal,
    'pays': pays,
    'avatar': avatar,
    'nomExploitation': nomExploitation,
    'descriptionExploitation': descriptionExploitation,
    'certifieBio': certifieBio,
    'verifie': verifie,
  };
}
